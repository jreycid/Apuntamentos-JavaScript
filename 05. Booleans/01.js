/*
 * Os booleano soamente admiten dous valores: verdadeiro e falso
 * Son útiles para comprobar o estado dunha condición ou variable
 *
 */

console.log(Boolean(1));
console.log(Boolean("Whatever"));
console.log(Boolean(3.14));
console.log(Boolean(100 > 5));
console.log(Boolean(1 < 100));
console.log(Boolean("1" == 1));

console.log(Boolean(0));
console.log(Boolean(-0));
console.log(Boolean(""));
console.log(Boolean(NaN));
console.log(Boolean(null));
console.log(Boolean(undefined));
console.log(Boolean("1" === 1));
